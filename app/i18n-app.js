function setLanguage(lang) {
  if (!translations[lang]) return;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const text = translations[lang][el.getAttribute("data-i18n")];
    if (text) el.innerHTML = text;
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const text = translations[lang][el.getAttribute("data-i18n-placeholder")];
    if (text) el.placeholder = text;
  });

  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  localStorage.setItem("nadini-lang", lang);
}

// Language buttons
document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll(".lang-btn").forEach(btn => {
    btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
  });

  const saved = localStorage.getItem("nadini-lang");
  const browserLang = navigator.language.slice(0, 2);
  setLanguage(saved || (translations[browserLang] ? browserLang : "de"));
});
